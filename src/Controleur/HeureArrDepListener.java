package Controleur;

import java.awt.event.ActionEvent;
import java.awt.event.ActionListener;

import Vue.Fenetre;

public class HeureArrDepListener implements ActionListener {

	private Fenetre fen;
	
	public HeureArrDepListener(Fenetre f) {
		this.fen = f;
	}
	
	@Override
	public void actionPerformed(ActionEvent e) {
		fen.updateHeureArr();
		fen.updateHeureDep();
	}

}
